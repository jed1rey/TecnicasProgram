package prova.prova;

/**
 *
 * @author paula
 */
public class ConversaoDeDados {

    public static double km_metros(double km) {
        return km * 1000;
    }

    public static double litro_decilitros(double litros) {
        return litros * 10;
    }

    public static double metro_pes(double metroCubico) {
        return metroCubico * 35.31;
    }

    public static double barril_decalitros(double barril) {
        return barril * 16.36;
    }

    public static double barril_litros(double barril) {
        return barril * 163.65;
    }

}
